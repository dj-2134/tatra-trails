// Pull trail geometry from OpenStreetMap via the BRouter routing API and emit
// UPDATE statements for the `hikes` table. The router snaps a path onto the real
// marked trails between the waypoints below. OSM data © OpenStreetMap contributors (ODbL).
//
// Usage:  node scripts/build-geometry.mjs        (writes db/update-geometry.sql)
// To add/refine a hike: add its slug + ordered [lon, lat] waypoints to ROUTES and re-run.
// For a loop, repeat the first waypoint at the end.
import { writeFileSync } from "node:fs";

const PROFILE = "hiking-beta"; // OSM-based foot/hiking profile on the public BRouter server

const ROUTES = {
  "strbske-lakeside-loop": [
    [20.0625, 49.1212], [20.0660, 49.1190], [20.0628, 49.1158], [20.0592, 49.1188], [20.0625, 49.1212],
  ],
  "strbske-popradske": [
    [20.0626, 49.1196], [20.0883, 49.1577],
  ],
  "hrebienok-zbojnicka": [
    [20.2316, 49.1585], [20.2067, 49.1906],
  ],
  // Ends at Chata pod Rysmi (the marked-trail hut just below the summit); the final
  // chained summit push is excluded by the hiking profile, so we stop at the hut.
  "popradske-rysy": [
    [20.0883, 49.1577], [20.0856, 49.1690], [20.0876, 49.1817],
  ],
};

async function fetchRoute(waypoints) {
  const lonlats = waypoints.map(([lon, lat]) => `${lon},${lat}`).join("|");
  const url = `https://brouter.de/brouter?lonlats=${lonlats}&profile=${PROFILE}&alternativeidx=0&format=geojson`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`BRouter HTTP ${res.status} for ${lonlats}`);
  const gj = await res.json();
  const feature = gj.features?.[0];
  if (!feature || feature.geometry?.type !== "LineString") throw new Error(`No LineString returned for ${lonlats}`);
  // Strip BRouter's elevation (3rd value) and round to 6 decimals -> clean 2D GeoJSON.
  const coords = feature.geometry.coordinates.map(([lon, lat]) => [
    Math.round(lon * 1e6) / 1e6,
    Math.round(lat * 1e6) / 1e6,
  ]);
  return { coords, lengthM: Number(feature.properties?.["track-length"]) || 0 };
}

const out = [
  "-- db/update-geometry.sql — accurate trail geometry from OpenStreetMap via BRouter.",
  "-- Generated by scripts/build-geometry.mjs. OSM data © OpenStreetMap contributors (ODbL).",
  "-- Run in Supabase Studio → SQL Editor to refresh existing rows (safe to re-run).",
  "",
];

const failures = [];
for (const [slug, waypoints] of Object.entries(ROUTES)) {
  try {
    const { coords, lengthM } = await fetchRoute(waypoints);
    const geojson = JSON.stringify({ type: "LineString", coordinates: coords });
    out.push(`-- ${slug}: ${coords.length} points, ~${Math.round(lengthM)} m`);
    out.push(`update hikes set geometry = '${geojson}'::jsonb, updated_at = now() where slug = '${slug}';`);
    out.push("");
    console.error(`OK   ${slug.padEnd(24)} ${String(coords.length).padStart(4)} pts  ~${Math.round(lengthM)} m`);
  } catch (e) {
    failures.push(slug);
    console.error(`FAIL ${slug.padEnd(24)} ${e.message}`);
  }
}

writeFileSync("db/update-geometry.sql", out.join("\n"));
console.error(`\nWrote db/update-geometry.sql (${Object.keys(ROUTES).length - failures.length}/${Object.keys(ROUTES).length} routes).`);
if (failures.length) {
  console.error(`Skipped (need waypoint tweaks): ${failures.join(", ")}`);
  process.exitCode = 1;
}
